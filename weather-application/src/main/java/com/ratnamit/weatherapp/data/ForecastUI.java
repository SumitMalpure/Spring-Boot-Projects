package com.ratnamit.weatherapp.data;

public class ForecastUI {

	String forecastDate;
	float lowTemp;
	float highTemp;
	/**
	 * @return the forecastDate
	 */
	public String getForecastDate() {
		return forecastDate;
	}
	/**
	 * @param forecastDate the forecastDate to set
	 */
	public void setForecastDate(String forecastDate) {
		this.forecastDate = forecastDate;
	}
	/**
	 * @return the lowTemp
	 */
	public float getLowTemp() {
		return lowTemp;
	}
	/**
	 * @param lowTemp the lowTemp to set
	 */
	public void setLowTemp(float lowTemp) {
		this.lowTemp = lowTemp;
	}
	/**
	 * @return the highTemp
	 */
	public float getHighTemp() {
		return highTemp;
	}
	/**
	 * @param highTemp the highTemp to set
	 */
	public void setHighTemp(float highTemp) {
		this.highTemp = highTemp;
	}
	
	
	
	
}
